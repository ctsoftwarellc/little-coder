#!/usr/bin/env node
import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join, relative, resolve } from "node:path";
import { pathToFileURL } from "node:url";

function walkPhp(dir) {
  if (!existsSync(dir)) return [];
  const out = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const path = join(dir, entry.name);
    if (entry.isDirectory()) out.push(...walkPhp(path));
    else if (entry.isFile() && entry.name.endsWith(".php")) out.push(path);
  }
  return out;
}

function phpFiles(repo) {
  return ["app", "routes", "tests"]
    .flatMap((dir) => walkPhp(join(repo, dir)))
    .sort();
}

function className(content, fallback) {
  return content.match(/\b(?:class|interface|trait|enum)\s+([A-Za-z_][A-Za-z0-9_]*)/)?.[1] ?? fallback;
}

function pageRank(edges, iterations = 20, damping = 0.85) {
  const nodes = Array.from(edges.keys());
  const rank = new Map(nodes.map((node) => [node, 1 / Math.max(nodes.length, 1)]));
  for (let i = 0; i < iterations; i++) {
    const next = new Map(nodes.map((node) => [node, (1 - damping) / Math.max(nodes.length, 1)]));
    for (const node of nodes) {
      const links = edges.get(node) ?? [];
      if (links.length === 0) continue;
      const share = (rank.get(node) ?? 0) / links.length;
      for (const link of links) next.set(link, (next.get(link) ?? 0) + damping * share);
    }
    for (const [node, value] of next) rank.set(node, value);
  }
  return rank;
}

export function generateRankMap(repoPath, outPath = join(repoPath, ".arcova", "RANKED_MAP.md")) {
  const repo = resolve(repoPath);
  if (!existsSync(join(repo, "artisan")) || !existsSync(join(repo, "composer.json"))) {
    throw new Error(`${repo} is not a Laravel repo (missing artisan or composer.json)`);
  }
  const files = phpFiles(repo);
  const symbols = new Map();
  const contents = new Map();
  for (const file of files) {
    const content = readFileSync(file, "utf-8");
    contents.set(file, content);
    symbols.set(className(content, file.split(/[\\/]/).pop()?.replace(/\.php$/, "") ?? file), file);
  }
  const edges = new Map(files.map((file) => [file, []]));
  for (const [file, content] of contents) {
    for (const [symbol, target] of symbols) {
      if (target !== file && new RegExp(`\\b${symbol}\\b`).test(content)) edges.get(file)?.push(target);
    }
  }
  const ranks = pageRank(edges);
  const lines = [
    "# Arcova Ranked PHP Map",
    "",
    "Generated by `scripts/arcova-rank-map.mjs`. Dependency-free PHP symbol/reference ranking.",
    "",
    "## Ranked Entrypoints",
    ...Array.from(ranks.entries())
      .sort((a, b) => b[1] - a[1])
      .slice(0, 80)
      .map(([file, score]) => `- ${score.toFixed(5)} ${relative(repo, file).replaceAll("\\", "/")}`),
    "",
  ];
  mkdirSync(join(repo, ".arcova"), { recursive: true });
  writeFileSync(outPath, lines.join("\n"));
  return outPath;
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  const repo = process.argv[2] || process.cwd();
  const out = generateRankMap(repo);
  console.log(`Generated ${relative(resolve(repo), out).replaceAll("\\", "/")}`);
}
